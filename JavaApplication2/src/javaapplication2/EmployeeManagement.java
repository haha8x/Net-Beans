    private String EncryptPassword()
    {
        String encrypted = "";
        try {
            MessageDigest MD;
            MD = MessageDigest.getInstance("MD5");
            byte[] pass = (new String(txtPassword.getPassword())).getBytes();
            encrypted = new String(MD.digest(pass));
        }
        catch (NoSuchAlgorithmException ex)
        {
            ex.printStackTrace();
        }

        return encrypted;
    }

    private void refreshData()
    {
        while(mdlData.getRowCount()!=0)
        {
            mdlData.removeRow(0);
        }
        selectAllData();
    }

    private void btnAddNewActionPerformed(java.awt.event.ActionEvent evt) {
        try {
        String password = EncryptPassword();

        pStmt = oConn.prepareStatement("Insert into tblEmployee(fullname,password,age,address) values(?,?,?,?)");

        pStmt.setString(1,txtFullname.getText());
        pStmt.setString(2, password);
        pStmt.setInt(3,Integer.parseInt(txtAge.getText()));
        pStmt.setString(4,txtaAddress.getText());
        pStmt.execute();

        refreshData();

        } catch (Exception e)
        {
            JOptionPane.showMessageDialog(null,"Cannot insert into database");
            e.printStackTrace();
        }
    }

