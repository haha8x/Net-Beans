    private void selectAllData()
    {
        try
        {
          pStmt = oConn.prepareStatement("select userID,password,fullname,age,address from tblEmployee");
          rsData = pStmt.executeQuery();

          while(rsData.next())
          {
              Vector row = new Vector();
              row.add(rsData.getString(1));
              row.add(rsData.getString(3));
              row.add(rsData.getString(4));
              row.add(rsData.getString(5));
              mdlData.addRow(row);
          }

        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null,"Cannot select data from database");
        }
    }

